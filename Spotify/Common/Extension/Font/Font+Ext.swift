import SwiftUI

extension Font {
    static func avenirNextBold(size: CGFloat) -> Font {
        Font.custom("AvenirNextLTPro-Bold", size: size)
    }
    
    static func avenirNextRegular(size: CGFloat) -> Font {
        Font.custom("AvenirNextLTPro-Regular", size: size)
    }
    
    static  func avenirNextDemi(size: CGFloat) -> Font {
        Font.custom("AvenirNextLTPro-Demi", size: size)
    }
}
